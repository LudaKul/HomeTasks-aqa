package School_OOP;

public class School {
    private int classNumber;
    private int countPupil;
    private String useSlogan;
    private String nameTeacher;

    public int getClassNumber() {
        return classNumber;
    }

    public void setClassNumber(int classNumber) {
        this.classNumber = classNumber;
    }

    public String getUseSlogan() {
        return useSlogan;
    }

    public void setNameTeacher(String nameTeacher) {
        this.nameTeacher = nameTeacher;
    }

    public void setUseSlogan(String useSlogan) {
        this.useSlogan = useSlogan;
    }

    public String getNameTeacher() {
        return nameTeacher;
    }

    public int getCountPupil() {
        return countPupil;
    }

    public void setCountPupil(int countPupil) {
        this.countPupil = countPupil;
    }

}
